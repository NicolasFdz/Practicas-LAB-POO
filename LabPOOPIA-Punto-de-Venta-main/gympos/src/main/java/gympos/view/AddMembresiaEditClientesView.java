    private void AgregarMembresiaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarMembresiaClienteActionPerformed

        // 1. Validar selección
        String nombreTipoSeleccionado = (String) OpcionesMembresias.getSelectedItem();
        if (nombreTipoSeleccionado == null || nombreTipoSeleccionado.contains("No hay")) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de membresía válido.", "Error de Datos", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            // 2. Obtener el objeto TipoMembresia completo de la lista local
            Optional<TipoMembresia> tipoOpt = tiposMembresiaDisponibles.stream()
                .filter(t -> t.getNombre().equals(nombreTipoSeleccionado))
                .findFirst();

            if (!tipoOpt.isPresent()) {
                // Este caso no debería ocurrir si cargarOpcionesMembresias funciona correctamente
                throw new ExcepcionDatos("Error interno: El tipo de membresía seleccionado no fue encontrado.");
            }
            
            TipoMembresia tipoSeleccionado = tipoOpt.get();
            
            // --- Parámetros para la nueva Membresia ---
            // El ID debe ser generado por el servicio para asegurar unicidad
            String nuevoId = membresiaService.generarNuevoIdMembresia(); 
            LocalDate fechaInicio = LocalDate.now();
            BigDecimal descuento = BigDecimal.ZERO;
            String descuentoText = txtDescuento.getText().trim();
            
            if (!descuentoText.isEmpty()) {
                try {
                    // Intentar parsear el texto a un número (puede ser entero o decimal)
                    BigDecimal valorIngresado = new BigDecimal(descuentoText);

                    // Validar que el valor sea >= 0 y <= 100
                    if (valorIngresado.compareTo(BigDecimal.ZERO) < 0 || valorIngresado.compareTo(new BigDecimal("100")) > 0) {
                        JOptionPane.showMessageDialog(this, "El descuento debe ser un valor numérico entre 0 y 100.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
                        return;
                    }

                    // Convertir el porcentaje (X) a un factor (X/100)
                    descuento = valorIngresado.divide(new BigDecimal("100.00"), 4, BigDecimal.ROUND_HALF_UP);

                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "El descuento debe ser un valor numérico válido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            
            // 3. Crear el nuevo objeto Membresia
            Membresia nuevaMembresia = new Membresia(nuevoId, tipoSeleccionado, fechaInicio, descuento);
            
            // 4. Agregar la membresía al cliente
            clienteAEditar.getMembresias().add(nuevaMembresia);
            
            // 5. Persistir los cambios del cliente
            clienteService.actualizarCliente(clienteAEditar);
            
            // 6. Refrescar la vista padre (EditClientesView)
            parentView.refrescarMembresias();
            
            // 7. Mostrar éxito y cerrar ventana
            JOptionPane.showMessageDialog(this, 
                "Membresía '" + tipoSeleccionado.getNombre() + "' agregada a " + clienteAEditar.getNombre() + ".", 
                "Éxito", JOptionPane.INFORMATION_MESSAGE);
            
            
        } catch (ExcepcionDatos ex) {
            logger.log(java.util.logging.Level.SEVERE, "Error al agregar membresía", ex);
            JOptionPane.showMessageDialog(this, 
                "Error al agregar la membresía: " + ex.getMessage(), 
                "Error de Datos", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
             logger.log(java.util.logging.Level.SEVERE, "Error inesperado", ex);
             JOptionPane.showMessageDialog(this, 
                "Error inesperado: " + ex.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AgregarMembresiaClienteActionPerformed

