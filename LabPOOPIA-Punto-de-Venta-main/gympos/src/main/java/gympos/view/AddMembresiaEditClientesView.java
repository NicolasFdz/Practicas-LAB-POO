    private void cargarOpcionesMembresias() {
        try {
            // 1. Obtener la lista de TiposMembresia del servicio
            this.tiposMembresiaDisponibles = membresiaService.getTiposMembresia();
            
            // 2. Crear un modelo para el JComboBox
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            
            if (tiposMembresiaDisponibles.isEmpty()) {
                model.addElement("No hay Tipos de Membresía disponibles");
                AgregarMembresiaCliente.setEnabled(false);
            } else {
                // 3. Agregar solo el nombre de cada TipoMembresia al modelo
                for (TipoMembresia tipo : tiposMembresiaDisponibles) {
                    model.addElement(tipo.getNombre());
                }
            }
            
            // 4. Asignar el modelo al JComboBox
            OpcionesMembresias.setModel(model);
            
        } catch (Exception ex) {
             logger.log(java.util.logging.Level.SEVERE, "Error al cargar tipos de membresía", ex);
             JOptionPane.showMessageDialog(this, 
                 "Error al cargar los tipos de membresía: " + ex.getMessage(), 
                 "Error de Carga", JOptionPane.ERROR_MESSAGE);
             AgregarMembresiaCliente.setEnabled(false);
        }
    }
    
