    private void cargarTodosLosClientes() 
    {
        List<Cliente> clientes = clienteService.getClientes();
        actualizarTabla(clientes);
    }
    
    private void actualizarTabla(List<Cliente> clientes)
    {
        // Limpiar filas anteriores
        this.tableModel.setRowCount(0);

        // Iterar sobre la lista de clientes y añadir cada uno como una fila
        for (Cliente cliente : clientes) 
        {
            Object[] fila = new Object[]{
                cliente.getIdCliente(),
                cliente.getNombre(),
                cliente.getApellido(),
                cliente.getEmail(),
                cliente.getPuntos()
            };

            this.tableModel.addRow(fila);
        }
    }
    
    public void refrescarTabla() {
        cargarTodosLosClientes();
    }
    
    /**
    * Obtiene el ID del cliente seleccionado en la tabla.
    * @return El ID del cliente, o null si no hay selección.
    */
   private String obtenerIdClienteSeleccionado() 
   {
       // Obtener el índice de la fila seleccionada
       int filaSeleccionada = jTable1.getSelectedRow();

       if (filaSeleccionada == -1) 
       {
           JOptionPane.showMessageDialog(this, 
               "Debe seleccionar un cliente de la tabla.", 
               "Error de Selección", 
               JOptionPane.WARNING_MESSAGE);
           return null;
       }

       // El ID se encuentra en la primera columna (índice 0)
       return (String) jTable1.getValueAt(filaSeleccionada, 0);
   }
    
