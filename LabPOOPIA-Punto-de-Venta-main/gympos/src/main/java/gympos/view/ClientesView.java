    private void BuscarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarUserActionPerformed
        // TODO add your handling code here:
        // 1. Pedir al usuario el término de búsqueda mediante un cuadro de diálogo
        String query = JOptionPane.showInputDialog(this, 
            "Ingrese el nombre o apellido a buscar:", 
            "Buscar Cliente", 
            JOptionPane.QUESTION_MESSAGE);
        
        // 2. Validar la entrada (si cancela o deja vacío)
        if (query == null || query.trim().isEmpty()) {
            // Si cancela o no ingresa nada, cargamos todos los clientes
            cargarTodosLosClientes();
            return;
        }
        
        // 3. Realizar la búsqueda usando ClienteService
        // El método buscarClientes busca por nombre o apellido (simula una búsqueda parcial)
        List<Cliente> resultados = clienteService.buscarClientes(query.trim());
        
        // 4. Actualizar la tabla con los resultados
        actualizarTabla(resultados);
        
        // 5. Mostrar un mensaje si no se encuentran resultados
        if (resultados.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "No se encontraron clientes que coincidan con la búsqueda: " + query.trim(), 
                "Búsqueda sin resultados", 
                JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BuscarUserActionPerformed

    private void ModificarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarUserActionPerformed
        String idCliente = obtenerIdClienteSeleccionado();
        
        if (idCliente == null) {
            return;
        }
        
        java.util.Optional<gympos.model.Cliente> clienteOpt = clienteService.obtenerClientePorId(idCliente);
        
        if (clienteOpt.isPresent()) 
        {
            gympos.model.Cliente clienteAEditar = clienteOpt.get();
            
            // 1. Instanciar EditClientesView: Pasamos el Cliente y la referencia de la vista padre
            EditClientesView editView = new EditClientesView(clienteAEditar, this); 
            editView.setVisible(true);
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, 
                "Error: No se pudo encontrar el cliente para modificar.", 
                "Error Interno", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ModificarUserActionPerformed

    private void EliminarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarUserActionPerformed
        // TODO add your handling code here:
        String idCliente = obtenerIdClienteSeleccionado();
        
        if (idCliente == null) {
            return; // Ya se mostró el mensaje de error de selección
        }
        
        // Confirmación antes de eliminar
        int confirmacion = JOptionPane.showConfirmDialog(this, 
            "¿Está seguro de que desea eliminar al cliente con ID: " + idCliente + "?", 
            "Confirmar Eliminación", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirmacion == JOptionPane.YES_OPTION) 
        {
            try 
            {
                // Llamada al servicio para eliminar
                clienteService.eliminarCliente(idCliente);
                
                // Refrescar la tabla
                cargarTodosLosClientes(); 
                
                JOptionPane.showMessageDialog(this, 
                    "Cliente " + idCliente + " eliminado exitosamente.", 
                    "Éxito", 
                    JOptionPane.INFORMATION_MESSAGE);
            } 
            catch (gympos.model.ExcepcionDatos ex) 
            {
                JOptionPane.showMessageDialog(this, 
                    "Error al eliminar el cliente: " + ex.getMessage(), 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_EliminarUserActionPerformed

